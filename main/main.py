# Numbers
a = int(input("Enter a value: "))

b = int(input("Enter another value: "))

print(a / b)  # classic division returns a float

print(a // b)  # floor division discards the fractional part

print(a % b)  # the % operator returns the remainder of the division

print(a ** b)  # use the ** operator to calculate powers

# There is full support for floating point; operators with mixed type operands convert the integer operand to floating point:
print(4 * 3.75 - 1)

